//arithmetic operators
#include <iostream>
using namespace std;

int main() {
    //Arithmetic operators
    //The five arithmetic operators supported by c++ are:

    /*
       + --> addition.
       - --> subtraction.
       * --> multiplication
       / --> division.
       % --> modulo.
    */

     int a,b,c,d,e;
     a = 10+20;
     b = a - 10;
     c = b * 5;
     d = c / a;
     e = a % b;

     cout<<"a = "<<a<<endl;
     cout<<"b = "<<b<<endl;
     cout<<"c = "<<c<<endl;
     cout<<"d = "<<d<<endl;
     cout<<"e = "<<e<<endl;


    return 0;
}